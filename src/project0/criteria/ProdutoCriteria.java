package project0.criteria;

public class ProdutoCriteria {

	
	private String nome;
	private Double prezoMin;
	private Double prezoMax;

	public ProdutoCriteria() {

	}


	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public Double getPrezoMin() {
		return prezoMin;
	}

	public void setPrezoMin(Double prezoMin) {
		this.prezoMin = prezoMin;
	}

	public Double getPrezoMax() {
		return prezoMax;
	}

	public void setPrezoMax(Double prezoMax) {
		this.prezoMax = prezoMax;
	}

}
